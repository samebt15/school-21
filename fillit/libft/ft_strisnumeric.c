#include "libft.h"

int	ft_strisnumeric(const char *s)
{
	if (s == NULL)
		return (0);
	while (s)
	{
		if (ft_isdigit(*s) || *s == '-')
			s++;
		else
			return (0);
	}
	return (1);
}
