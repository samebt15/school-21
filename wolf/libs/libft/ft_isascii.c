#include "libft.h"

int		ft_isascii(int c)
{
	int	i;

	i = 0;
	if ((c < 0) && (c > 127))
		return (0);
	while (i <= 127)
	{
		if (i == c)
		{
			return (1);
		}
		i++;
	}
	return (0);
}
