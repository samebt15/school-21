#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *src;
	unsigned char c1;

	src = (unsigned char *)s;
	c1 = (unsigned char)c;
	while (n)
	{
		if (*src == c1)
			return (src);
		src++;
		n--;
	}
	return (NULL);
}
