#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *buf;

	buf = (unsigned char *)b;
	while (len--)
	{
		*buf = (unsigned char)c;
		buf++;
	}
	return (b);
}
