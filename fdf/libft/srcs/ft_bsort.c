#include "libft.h"

void	ft_bsort(int *arr, int l)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (i < l - 1)
	{
		j = 0;
		while (j < l - i - 1)
		{
			if (arr[j] > arr[j + 1])
				ft_swap(&arr[j], &arr[j + 1]);
			j++;
		}
		i++;
	}
}
