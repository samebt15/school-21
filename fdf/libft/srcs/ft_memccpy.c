#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int ch, size_t n)
{
	size_t			i;
	unsigned char	*dest;
	unsigned char	*source;

	dest = (unsigned char*)dst;
	source = (unsigned char*)src;
	i = 0;
	while (i < n)
	{
		dest[i] = source[i];
		if (source[i] == (unsigned char)ch)
			return (dest + i + 1);
		i++;
	}
	return (0);
}
