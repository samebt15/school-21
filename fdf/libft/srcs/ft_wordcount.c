#include "libft.h"

int			ft_wordcount(const char *str, char c)
{
	int		i;
	int		w;

	i = 1;
	w = 0;
	if (str[0] != c && str[0] != '\0')
		w += 1;
	while (str[i])
	{
		if (str[i - 1] == c && str[i] != c)
			w++;
		i++;
	}
	return (w);
}
