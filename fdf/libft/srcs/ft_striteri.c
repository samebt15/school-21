#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t			i;
	unsigned int	j;

	j = 0;
	i = 0;
	if (s && f)
		while (s[i])
		{
			f(j, &s[i]);
			i++;
			j++;
		}
}
