#include "libft.h"
#include <string.h>

char	*ft_strstr(const char *s1, const char *s2)
{
	int				j;
	int				i;

	if (ft_strlen(s2) == 0)
		return ((char *)s1);
	if (ft_strlen(s1) == 0)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		j = 0;
		while (s1[i + j] == s2[j] && s2[j] != '\0')
			j++;
		if (s2[j] == '\0')
			return ((char *)s1 + i);
		i++;
	}
	return (NULL);
}
