#include <string.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*res;
	unsigned char	*sr;

	if (!dst && !src && n)
		return (NULL);
	res = (unsigned char *)dst;
	sr = (unsigned char *)src;
	i = -1;
	while (++i < n)
		*(res + i) = *(sr + i);
	return (dst);
}
