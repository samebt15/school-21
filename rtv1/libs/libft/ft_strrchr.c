#include "libft.h"

char	*ft_strrchr(const char *string, int symbol)
{
	char *res;

	res = NULL;
	while (*string != '\0')
	{
		if (*string == symbol)
			res = (char*)string;
		string++;
	}
	if (symbol == '\0')
		res = (char*)string;
	return (res);
}
